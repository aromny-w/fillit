#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char	*res;
	size_t	i;

	res = s1;
	i = 0;
	while (*s1)
		s1++;
	while (*s2 && i < n)
	{
		*s1++ = *s2++;
		++i;
	}
	*s1 = '\0';
	return (res);
}
